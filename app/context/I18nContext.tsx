function normalizeLang(value: unknown): AppLang {
  return value === "ar" ? "ar" : "en";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const cookies = useMemo(() => parseCookies(), []);
  const [lang, setLangState] = useState<AppLang>(() => normalizeLang(cookies.appLang));

  const setLang = useCallback((next: AppLang) => {
    setLangState(next);
    setCookie(null, "appLang", next, {
      maxAge: 365 * 24 * 60 * 60,
      path: "/",
      secure: process.env.NODE_ENV === "production",
      sameSite: "lax",
    });
    try {
      localStorage.setItem("appLang", next);
    } catch {
      // ignore
    }
  }, []);

  useEffect(() => {
    // If cookie missing but localStorage has value (first load after deploy), adopt it.
    try {
      const ls = localStorage.getItem("appLang");
      if (ls && normalizeLang(ls) !== lang) setLang(normalizeLang(ls));
    } catch {
      // ignore
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const dir: "ltr" | "rtl" = lang === "ar" ? "rtl" : "ltr";

  useEffect(() => {
    // Make RTL actually affect the whole app without server-side i18n setup.
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
  }, [lang, dir]);

  const t = useCallback(
    (key: string) => DICT[lang]?.[key] ?? DICT.en[key] ?? key,
    [lang]
  );

  const value = useMemo<I18nContextValue>(() => ({ lang, dir, setLang, t }), [lang, dir, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}


